# todo: Проверить истинность высказывания: "Данное четырехзначное число читается одинаково
# слева направо и справа налево".
word = input('Введите четырехзначное число:')
right_left= word[::-1]
if right_left == word:
    print('Данное четырехзначное число читается одинаково слева направо и справа налево')
else:
    print('Данное четырехзначное число читается неодинаково')
